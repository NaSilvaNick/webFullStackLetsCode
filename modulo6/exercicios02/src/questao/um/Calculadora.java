package questao.um;

public class Calculadora {

    public static float soma(float primeiroValor, float segundoValor){

        return primeiroValor + segundoValor;
    }

    public static float subtracao(float primeiroValor, float segundoValor){

        return primeiroValor - segundoValor;
    }

    public static float multiplicacao(float primeiroValor, float segundoValor){

        return primeiroValor * segundoValor ;
    }

    public static float divisao(float primeiroValor, float segundoValor){

        return primeiroValor / segundoValor;
    }
}
